class SynonymExpander:
    def expand(self, text: str) -> str:
        result = text

        for old, new in MEDICAL_REPLACEMENTS.items():
            result = re.sub(rf"\b{re.escape(old)}\b", f" {new} ", result)

        return re.sub(r"\s+", " ", result).strip()