-- ============================================================
-- Миграция 002 — версионирование цен (ТЗ 4.4 / 5)
-- ============================================================
-- Применение:  psql "$DATABASE_URL" -f sql/migrations/002_price_versioning.sql
--
-- Меняет стратегию дедупа с «партнёр+услуга+дата» на «одна активная версия
-- услуги у партнёра». История версий хранится строками is_active=FALSE и не
-- удаляется. Загрузчик (repository._save_item_versioned) при новой цене
-- архивирует старую активную версию и вставляет новую.
-- ============================================================

-- Старый дедуп по дате больше не нужен: он держал по одной активной строке на
-- (партнёр, услуга, дата) и мешал версионированию в рамках одной даты.
DROP INDEX IF EXISTS uq_item_dedup;

-- Одна активная (актуальная) версия услуги у партнёра.
CREATE UNIQUE INDEX IF NOT EXISTS uq_item_active
    ON price_item (partner_id, service_name_raw)
    WHERE is_active = TRUE;

-- Быстрый поиск конкретной версии при идемпотентной перезагрузке и при
-- построении истории (partner + service + дата).
CREATE INDEX IF NOT EXISTS idx_item_version
    ON price_item (
        partner_id,
        service_name_raw,
        (COALESCE(effective_date, DATE '0001-01-01'))
    );
